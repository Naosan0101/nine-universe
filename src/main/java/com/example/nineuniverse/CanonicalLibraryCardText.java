	public static String lineForId(short id) {
		if (id < 1 || id >= BY_ID.length) {
			return null;
		}
		return BY_ID[id];
	}

	/**
	 * 正規1行から「フィールド／配置／常時／効果なし」の本文だけを取り出す（モーダルで名前・数値と分離表示するため）。
	 */
	public static String abilityTextForId(short id) {
		String full = lineForId(id);
		if (full == null || full.isEmpty()) {
			return "";
		}
		if (full.endsWith("/効果なし。") || full.endsWith("/能力なし。")) {
			return "効果なし。";
		}
		String[] markers = {"/フィールド：", "/フィールド:", "/配置：", "/配置:", "/常時：", "/常時:"};
		for (String m : markers) {
			int i = full.indexOf(m);
			if (i >= 0) {
				return full.substring(i + m.length());
			}
		}
		return full;
	}
