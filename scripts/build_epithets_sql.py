def lines(blob: str) -> list[str]:
    out: list[str] = []
    seen: set[str] = set()
    for line in blob.splitlines():
        s = line.strip()
        if not s:
            continue
        if s in seen:
            continue
        seen.add(s)
        out.append(s)
    return out


def esc(s: str) -> str:
    return s.replace("'", "''")


